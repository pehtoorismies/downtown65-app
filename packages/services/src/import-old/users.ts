const userHashmap: Record<string, ImportedUser> = {}

export const getUserById = (userId: string): ImportedUser | undefined => {
  if (userHashmap[userId]) {
    return userHashmap[userId]
  }

  const user = users.find(({ id }) => userId === id)
  if (!user) {
    return
  }

  userHashmap[userId] = user
  return user
}
